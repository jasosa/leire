package cat.hmobile.leire.entities.products;

import cat.hmobile.leire.entities.LeireException;

public class BrandNotFoundException extends LeireException {
	
	private String m_brand;
	
	public BrandNotFoundException(String brand)
	{
		super();
		this.m_brand = brand;
	}
	
	public String getBrand()
	{
		return m_brand;
	}
}
