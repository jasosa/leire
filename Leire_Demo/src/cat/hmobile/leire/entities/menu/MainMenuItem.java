package cat.hmobile.leire.entities.menu;

public class MainMenuItem {
	private int m_itemId;
	private int m_itemDescriptionId;
	private int m_itemDrawableIconId;
	private boolean m_itemEnabled;
	
	public MainMenuItem(int id, int descId, int drawableId, boolean enabled){
		m_itemId = id;
		m_itemDescriptionId = descId;
		m_itemDrawableIconId = drawableId;
		m_itemEnabled = enabled;
	}
	
	public int get_itemId() {
		return m_itemId;
	}
	
	public int get_itemDescriptionId() {
		return m_itemDescriptionId;
	}
	
	
	public int get_itemDrawableIconId() {
		return m_itemDrawableIconId;
	}

	public void set_itemEnabled(boolean m_itemEnabled) {
		this.m_itemEnabled = m_itemEnabled;
	}

	public boolean is_itemEnabled() {
		return m_itemEnabled;
	}
}
